    def _format_san_chuan(self, san_chuan):
        """格式化三传数据为可读文本"""
        if not san_chuan:
            return "未知"
        
        try:
            # 如果是字典格式
            if isinstance(san_chuan, dict):
                chu_chuan = san_chuan.get('chu_chuan', {})
                zhong_chuan = san_chuan.get('zhong_chuan', {})
                mo_chuan = san_chuan.get('mo_chuan', {})
                
                chu_text = f"{chu_chuan.get('zhi', '')}{chu_chuan.get('meaning', '')}" if chu_chuan else "未知"
                zhong_text = f"{zhong_chuan.get('zhi', '')}{zhong_chuan.get('meaning', '')}" if zhong_chuan else "未知"
                mo_text = f"{mo_chuan.get('zhi', '')}{mo_chuan.get('meaning', '')}" if mo_chuan else "未知"
                
                return f"初传{chu_text}，中传{zhong_text}，末传{mo_text}"
            
            # 如果是字符串格式
            elif isinstance(san_chuan, str):
                return san_chuan
            
            # 其他格式
            else:
                return str(san_chuan)
                
        except Exception:
            return "三传信息"
    
